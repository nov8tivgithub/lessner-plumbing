        <div class="grbgcon">
        	<p class="polyp fadeInLeft wow pdb15">Polybutylene pipe is also known as “water service pipe”, “yard service pipe” or “poly pipe”. It is installed underground and delivers public drinking water from the street into your home. Polybutylene pipe is commonly used in and around homes the Baltimore metro area. Additionally, some polybutylene pipe is installed in the interior of homes forming the home plumbing system, but it is less common in the Baltimore area. Polybutylene pipe is blue or gray plastic pipe manufactured by the Shell Oil Corporation. It was installed in homes between 1978 and 1995, generally in new home construction. It was also used to replace water service in older homes. Chlorine in our drinking water (which is added to sanitize) reacts with the polybutylene making the pipe flake and become brittle. All polybutylene pipes will fail for this reason and the question is when, not if, it will happen. Polybutylene pipes seem to fail more often when the seasons change as a result of the ground temperature changing, causing the pipes to expand and contract aggravating the weakness.</p>
